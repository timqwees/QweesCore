namespace App\Controllers;
use App\Models\Network\Network;
use App\Models\Network\Message;

class MailController
{
    /**
     * Отправка электронного письма получателю.
     *
     * Выполняет проверку валидности email-адреса, темы и содержимого письма.
     * Подготавливает письмо и осуществляет его отправку через Network::onPHPMailer.
     * В случае возникновения ошибки возвращает false и записывает сообщение об ошибке.
     *
     * @param string $to_mail  Электронный адрес получателя.
     * @param string $subject  Тема письма.
     * @param string $body     Содержимое письма (HTML или текст).
     * @return bool|mixed      true при успешной отправке, false в случае ошибки.
     *
     * @example $this->onMail('you@wondermail.com', 'Подписка активирована!', 'Добро пожаловать!')
     */
    public function onMail(string $to_mail, string $subject, string $body)
    {
        if (empty($to_mail)) {
            message::set('error', "Пустой email получателя!");
            return false;
        } elseif (empty($subject)) {
            message::set('error', "Пустая тема письма!");
            return false;
        } elseif (empty($body)) {
            message::set('error', "Пустое содержание письма!");
            return false;
        }

        $mailer = [
            "to_email" => $to_mail,//Получатель
            "subject" => $subject,//заголовок
            "body" => $body,//сообщение
        ];

        try {
            return (new Network())->onPHPMailer($mailer);//отправка запроса/send request;
        } catch (\Exception $e) {
            message::set('error', "Ошибка отправки письма: " . $e->getMessage());
            return false;
        }
    }
}